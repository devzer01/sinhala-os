[SECTION .text] ; begin sector 1
BootStageOne:
    mov ah, 0h
    mov al, 11h ; AH=0 (Change video mode), AL=13h (Mode)
    int 10h ; Video BIOS interrupt

    mov ah, 02h
    mov al, 1 ;one sector
    mov ch, 0 ;track 1
    mov cl, 2 ;sector number 2
    mov dh, 0 ; head 0
    mov dl, 80h ;hard drive
    mov bx, STAGE2 ;
    int 13h
    mov ah, 02h
    mov cl, 3 ; sector 3
    mov al, 1 ; 4 sectors
    mov bx, VARS
    int 13h
    mov ah, 02h
    mov cl, 4 ; sector 3
    mov al, 4 ; 4 sectors
    mov bx, FONTDATA
    int 13h
    jmp STAGE2
    times 510-($-$$) db 0
end:
    dw 0xAA55 ;end of sector 1

section .text
fontdata:
    mov si, FONTDATA
    call writechar
    mov si, FONTDATA+16
    call writechar
    mov si, FONTDATA+765+35
    call writechar
    mov si, FONTDATA+960+96+33
