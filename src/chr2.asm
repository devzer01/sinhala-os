    mov si, FONTDATA+848
    call writechar
    mov si, FONTDATA+672
    call writechar

hl:
    nop
    jmp hl

writechar:
    mov ah, BIOSPIXEL ;bios
    mov bh, CURRENTPAGE ;page
    mov al, FONTHEIGHT
    mov [HEIGHT], al
    mov al, 0 ;test here if we need to move cursor back
    mov di, si ;di current font pointer
    mov cx, TOPBOTTOM;
    push ax
    push di
    push cx
sx: scasb ;if any of the next 4 bytes is not 0 it means we have top
    jne revcol
    loop sx
    jmp setcursor
revcol:
    mov ax, [COLS]
    sub ax, FONTWIDTH
    mov [COLS], ax
    mov al, TOPBOTTOM
    mov [HEIGHT], al ;we are dealing with half char
    sub byte [BITEND], FONTWIDTH
setcursor:
    pop cx
    pop di
    pop ax
    add di, 12
sy: scasb
    jne fwdrow
    loop sy
    jmp setcursor1
fwdrow:
    add WORD [ROW], 12
    sub WORD [COLS], FONTWIDTH
    sub byte [BITEND], FONTWIDTH
    ;mov al, 4
    ;mov [HEIGHT], al
    mov si, di
setcursor1:
    mov dx, [ROW] ;cursor
    mov cx, [COLS] ;cursor
rex:
    mov al, [si]
    mov [CURRENT], al
    push dx ;save cursor
    push cx ;save cursor
    call drawbits
    pop cx ;restore cursor
    pop dx ;restore cursor
    inc dx ;next row
    inc si ;next row
    cmp dx, [HEIGHT]    ;height
    jne rex
    ; we are done with one char, set the cursor position
    add byte [COLS], FONTWIDTH + 1
    add byte [BITEND], FONTWIDTH + 1
    mov word [ROW], 0x0001
    ret

drawbits:
bitloop:
    mov al, [CURRENT]
    and al, [INDEX]
    cmp al, [INDEX]
    jne skipint
    mov al, 1h
    mov ah, BIOSPIXEL
    int 10h
skipint:
    inc cl
    cmp cl, [BITEND]
    je rowend
    shr byte [INDEX], 1
    jmp bitloop
rowend:
    mov BYTE [INDEX], BITMASK
    ret
    times 510-($-fontdata) db 0
    dw 0xCCCC ;end of sector 2

