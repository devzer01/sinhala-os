hl:
    nop
    jmp hl

writechar:

    mov ah, 0ch
    mov bh, 0x0
    mov dx, 1h
    mov cx, 1h
rex:
    mov al, [si]
    push dx
    push cx
    call drawbits
    pop cx
    pop dx
    inc dx
    inc si
    cmp dx, 0x10
    jne rex
    ret

drawbits:
    push ax
    mov al, 0x80
    mov [index], al
bitloop:
    pop ax
    push ax
    and al, [index]
    cmp al, [index]
    jne skipint
    mov al, 1h
    mov ah, 0ch
    int 10h
skipint:
    inc cx
    cmp cx, 0x09
    je rowend
    shr byte [index], 1
    jmp bitloop
rowend:
    pop ax
    ret

