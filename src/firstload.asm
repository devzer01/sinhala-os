    int 10h
    push 0a000h
    pop es
    mov ax, chars
    mov si, ax
    mov dx, 16
    mov bx, 12
    xor di,di
loop2:
    push di
charloop:
    xor al, al
    cmp [ds:si], al
    jne setdraw
    jmp skipdraw
setdraw:
    mov al, 03h
    ; draw 5 pixels with "ah:al" color, ++color, di += 5
skipdraw:
    mov [es:di],al
    inc di
    inc si
    dec dx
    jnz charloop
    pop di
    add di, 320 ; move di to start of next line
    mov dx, 16
    dec bx     ; do next single pixel line
    jnz loop2
    xor ah,ah
    int 16h
    hlt

